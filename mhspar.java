public class mhspar{
	String a,c,b,d;//anggota class
	public mhspar(String nama,String nim,String fak,String prodi){//construktor parameter
		a = nama;
		b = nim;
		c = fak;
		d = prodi;
	}
public static void main(String[] args ){
	 mhspar mhs1 = new  mhspar("VANI VABRIANA","13020180086","ILMU KOMPUTER","TEKNIK INFPRMATIKA");//konstruktor parameter
	System.out.println("Nama 	 : " +(mhs1.a));
	System.out.println("Nim 	 : " +(mhs1.b));
	System.out.println("Fakultas : " +(mhs1.c));
	System.out.println("Prodi    : " +(mhs1.d));
}
}









































































































































































































